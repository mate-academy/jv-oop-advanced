package core.basesyntax;

public class IsoscelesTrapezoid extends Quadrangle {
    public IsoscelesTrapezoid(String color, int width, int height) {
        super(color, width, height);
    }

    @Override
    public double getArea() {
        return super.getArea();
    }

    @Override
    public String draw() {
        return "Figure: isoscelesTrapezoid" + super.draw();
    }
}
